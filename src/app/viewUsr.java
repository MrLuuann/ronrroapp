    private void formWindowActivated(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowActivated
        /*
        Este es el metodo que se inicializa cada que la ventana esta activa, empieza creando un modelo de la tabla creada, para poder modificarla
        ademas agrega las columnas que se mostraran
        */
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn( "username");
        model.addColumn( "id_persona");
        model.addColumn( "nombre");
        model.addColumn( "apellido1");
        model.addColumn( "apellido2");
        model.addColumn( "email");
        tblUsers.setModel(model);
        /*
        Se inicializan las variables del arreglo que guardara temporalmente cada fila
        */
        String[] fila = new String[6];
        
        Connection cn = null;
        PreparedStatement pst = null;
        ResultSet rs = null;
        try
        {
            /*
            Se conecta a la base de datos y se hace la consulta para obtener los datos de las personas
            */
            cn = conexion.conectar();
            String sql = "SELECT users.username, users.id_persona, personas.nombre, personas.apellido1, personas.apellido2, personas.email FROM users INNER JOIN personas ON users.id_persona = personas.id_persona;";            
            pst = cn.prepareStatement(sql);                        
            rs = pst.executeQuery();
            /*
            Con este ciclo va guardando cada registro fila por fila y lo almacena temporalmente en el arreglo para despues agregarlo a la tabla visible
            */
            while(rs.next()){
                fila[0] = rs.getString( "username");
                fila[1] = rs.getString( "id_persona");
                fila[2] = rs.getString( "nombre");
                fila[3] = rs.getString( "apellido1");
                fila[4] = rs.getString( "apellido2");
                fila[5] = rs.getString("email");
                model.addRow(fila);
            }
            
        }
        catch(SQLException e)
        {
            
            JOptionPane.showMessageDialog(null,"Error: " +e);
            
        }
        
        finally
        {
            try
            {
                /*
                cierra la conexion
                */
                if (rs != null) rs.close();
                
                if (pst != null) pst.close();
                
                if (cn != null) cn.close();
            }
            catch(SQLException e)
            {
                JOptionPane.showMessageDialog(null,e);
            }
        }
    }//GEN-LAST:event_formWindowActivated

